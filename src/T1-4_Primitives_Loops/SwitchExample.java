public class SwitchExample {

  public static void main(String[] args) {
    String option = "zero";
    switch (option){
      case "zero":
          System.out.println("option 1");
        break;
      case "one":
          System.out.println("option 2");
        break;
      case "two":
          System.out.println("option 3");
        break;



    }
  }
}
