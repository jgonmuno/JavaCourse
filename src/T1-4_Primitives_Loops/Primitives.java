public class Primitives {

  public static void main(String[] args) {

    byte numberByte = 127;
    System.out.println ("byte number is " +numberByte);
    System.out.println("byte type corresponds to Byte " +Byte.BYTES);
    System.out.println("byte type corresponds to Bites " +Byte.SIZE);
    System.out.println("maximum value of a byte" +Byte.MAX_VALUE);
    System.out.println("minimum value of a byte" +Byte.MIN_VALUE);

    short numberShort = 30000;
    System.out.println ("short number is " +numberShort);
    System.out.println("short type corresponds to Byte " +Short.BYTES);
    System.out.println("byte type corresponds to Bites " +Short.SIZE);
    System.out.println("Maximum value of a Short number " +Short.MAX_VALUE);
    System.out.println("Minimum value of a short number " +Short.MIN_VALUE);

    int numberInt = 2147483647;
    System.out.println ("Integer number is " +numberInt);
    System.out.println("Integer type corresponds to Byte " +Integer.BYTES);
    System.out.println("Integer type corresponds to Bites " +Integer.SIZE);
    System.out.println("Maximum value of a Integer number " +Integer.MAX_VALUE);
    System.out.println("Minimum value of a Integer number " +Integer.MIN_VALUE);
  }
}
